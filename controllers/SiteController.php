<?php

namespace app\controllers;

use app\models\Assessment;
use app\models\Group;
use app\models\Student;
use app\models\Subject;
use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        /** aktiv talabalarning umumiy soni */
        $active_student = Student::find()
            ->asArray()
            ->where(['status' => true])
            ->all();

        /** aktiv guruhlarning umumiy soni umumiy soni */
        $active_group = Group::find()
            ->asArray()
            ->where(['status' => true])
            ->all();
        /** aktiv fanlarning umumiy soni umumiy soni umumiy soni */
        $active_subject = Subject::find()
            ->asArray()
            ->where(['status' => true])
            ->all();

        /** Talabalarning nb lar soni */
        $student_nb = Assessment::find()
            ->asArray()
            ->where(['status'=>true, 'rating' => 'nb'])
            ->all();

//        echo '<pre>';
//        print_r($student_nb);
//        echo '</pre>';
//        exit();

        return $this->render('index', [
            'students' => $active_student,
            'groups' => $active_group,
            'subjects' => $active_subject,
            'nbs' => $student_nb
        ]);
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        return $this->render('about');
    }
}
